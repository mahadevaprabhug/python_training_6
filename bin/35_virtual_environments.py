# Python Installation and Virtual Environments
# --------------
# OPTION-1: Python Installation
#   - We can install multiple versions of python in our laptop
#
# OPTION-2: Virtual Environments
#   - We can install one version of python
#       then
#       we can create multiple copies of that installation
#       - This copies of python installation are called VIRTUAL ENVIRONMENTS
#
# If we are working multiple projects then
# project-1 -> create virtual environment-1 and use
# project-2 -> create virtual environment-2 and use
# etc
#
# So that for each project we are maintaining separate copy of python installation
#   folder. So, if something goes wrong in project-1 virtual environment
#   then it will not disturb project-2 virtual environment
################################

# How to create virtual environment
# --------------
# OPTION-1: If we are using some IDE then check their documentation
#   on how to create virtual environments on that IDE
#
# OPTION-2: If we are not Using any IDE OR IDE is not providing option
#           to create virtual environment
#           then
#           use command prompt to create
#           command: python -m venv myvenv1
################################

# How to create virtual environment in pycharm
# --------------
# File -> Settings -> project: python_training -> python-interpreter
#   -> add local interpreter
